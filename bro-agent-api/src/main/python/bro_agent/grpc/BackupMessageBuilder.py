class BackupMessageBuilder:
    """
     * Builds a message holding a file name.
     * @param fileName to be sent.
     * @return message.
    """
    @staticmethod
    def get_file_name_message(file_name):
        pass

    """
     * Builds a message holding bytes.
     * @param data to be sent.
     * @return message.
    """
    @staticmethod
    def get_data_message(data):
        pass

    """
     * Builds a message holding checksum.
     * @param checksum to be sent.
     * @return message.
    """
    @staticmethod
    def get_checksum_message(checksum):
        pass
