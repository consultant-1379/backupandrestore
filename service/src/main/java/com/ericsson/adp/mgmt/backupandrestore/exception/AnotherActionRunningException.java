package com.ericsson.adp.mgmt.backupandrestore.exception;

/**
 * Represent the Exception BRO throws when an action attempted to execute while another action is running
 */
public class AnotherActionRunningException extends RuntimeException{

    private static final long serialVersionUID = 8106606893286276129L;

    /**
     * Constructs a new runtime exception with the specified detail message.
     * The cause is not initialized, and may subsequently be initialized by a
     * call to {@link #initCause}.
     *
     * @param message the detail message. The detail message is saved for
     *                later retrieval by the {@link #getMessage()} method.
     */
    public AnotherActionRunningException(final String message) {
        super(message);
    }
}
